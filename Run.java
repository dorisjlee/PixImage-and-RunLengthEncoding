//The following Class represent a "Run" object as described in readme
//Contains:
//Run constructor that stores RGB values
public class Run {
	public int red;
	public int green;
	public int blue;
	public int length;
	public Run ( int r ,int g,int b,int run_length){
		red = r;
		green = g;
		blue = b;
		length = run_length;
	}
}